const booleanVariableTrue = true;
const booleanVariableFalse = false;
console.log(booleanVariableTrue);

if (booleanVariableTrue) {
    console.log("Tak ta zmienna ma wartośc true, czyli prawda.")
} else {
    console.log("Ten napis nie ma prawa się wyświetlić.")
}
if (booleanVariableFalse) {
    console.log("Tak ta zmienna ma wartośc true, czyli prawda. Ale ten napis, nie powinien się wyświetlać.")
} else {
    console.log("Ten napis powinien się wyświetlić. Druga zmienna ma wartość false.")
}